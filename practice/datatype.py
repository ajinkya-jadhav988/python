x = 5
y = 3.14
z = 2 + 3j

print(type(x), type(y), type(z))

# sequence type
text = "Hello, World"
my_list = [1, 2, 3]
my_tuple = (1, 2, 3)

print(type(text), type(my_list), type(my_tuple))

#mapping 
my_dict = {'name': 'John', 'age': 30}
print(type(my_dict)) 


#set

my_set = {1, 2, 3}
my_frozenset = frozenset([1, 2, 3])

print(type(my_set), type(my_frozenset))


# boolean

is_valid = True
print(is_valid)

# binary types

data = b'Hello'
data_mutable = bytearray(b'Hello')
print(type(data), type(data_mutable))

#none

Nothing = None 
print(type(Nothing))


